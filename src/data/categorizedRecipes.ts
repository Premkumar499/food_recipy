// Function to search for recipes across all categories
export const searchInCategories = (query: string): { category: string; recipes: string[] }[] => {
  if (!query.trim()) return [];
  
  const results: { category: string; recipes: string[] }[] = [];
  const lowerQuery = query.toLowerCase();
  
  Object.entries(categorizedRecipes).forEach(([category, recipes]) => {
    const matchingRecipes = recipes.filter(recipe => 
      recipe.toLowerCase().includes(lowerQuery)
    );
    
    if (matchingRecipes.length > 0) {
      results.push({
        category,
        recipes: matchingRecipes.slice(0, 5) // Limit to 5 results per category
      });
    }
  });
  
  return results;
};

// Function to get all recipes as flat array for search
export const getAllRecipes = (): string[] => {
  const allRecipes: string[] = [];
  Object.values(categorizedRecipes).forEach(recipes => {
    allRecipes.push(...recipes);
  });
  return allRecipes;
};
